async function seedPlantData() {
  try {
    await mongoose.connect(MONGODB_URI);
    console.log("Connected to MongoDB");

    // Clear existing plant info
    await PlantInfo.deleteMany({});
    console.log("Cleared existing plant data");

    // Insert new plant data
    const result = await PlantInfo.insertMany(plantData);
    console.log(`Successfully inserted ${result.length} plants`);

    // Display summary
    result.forEach((plant) => {
      console.log(`✓ ${plant.commonNames[0]} (${plant.scientificName})`);
    });

    await mongoose.disconnect();
    console.log("Seeding completed successfully!");
  } catch (error) {
    console.error("Error seeding plant data:", error);
    process.exit(1);
  }
}

